function ShowGruposA(id) {
  $.ajax({
    url: '/showGruposA',
    type: 'GET',
    dataType: 'json',
    success: 'success',
    data: { 'id': id },
    success: function (data) {
      $(".inforcao_projeto").empty();
      $(".inforcao_projeto").append(data.html);
    }
  });

  $(".add_mensagem").click(function () {
    $(".addMensagem").show()
  });
}

function verMensagens(id) {
  $(".forumDuvidas").hide();
  $(".divMensagens").show();
  $.ajax({
    url: '/verMensagens',
    type: 'GET',
    dataType: 'json',
    success: 'success',
    data: { 'id': id },
    success: function (data) {
      $(".divMensagens").empty();
      $(".divMensagens").append(data.html);
    }
  });
}

